md"""
#### Solución

a) Al tratarse de partículas de tamaño medio utilizaremos la __ley de Bond__.

b) Los datos del ensayo previo que proporciona el enunciado del problema son:
"""

# ╔═╡ baed4264-c6c9-4c62-acc5-3fb6f0894b68
L₁ = 0.8u"cm"

# ╔═╡ 6043a3f9-aaf6-4fa8-bd0b-ace093be4b13
