md"""
c) El volumen del prisma de la figura es:

$$V_p = \frac{\sqrt{3}}{4} a^3$$

Para obtener este volumen, encontramos primero el área del triángulo de la base. Se trata de un triángulo rectángulo de base $a$. Para calcular el área necesitamos la altura $h$, que podemos encontrar dividiendo el triánglo equilatero en dos triángulos rectángulos, de base $\frac{a}{2}$ e hipotenusa $a$. 
"""

