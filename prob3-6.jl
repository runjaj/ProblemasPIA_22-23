# ╔═╡ 50c28293-d510-495f-ba87-ed1f74bdd130
Lᵇ₁ = 0.6u"cm"

# ╔═╡ b407d9cf-cdd3-46cd-bd37-9439d52d68df
Eᵇ = 10u"kJ/kg"

# ╔═╡ 7d2d8644-d663-47b3-9378-54b584bf88fe
