# ╔═╡ c4f1817c-98d0-49a8-a1d9-f01df00898b2
md"""
A partir de estos datos podemos calcular $E_i$:
"""

# ╔═╡ b305e31a-85b1-4a0d-bc99-1bd4eda4a430
q = L₁/L₂

# ╔═╡ cf9e4077-808b-4cfd-ad2b-e30ac37cc0f1
Eᵢ = E/((100u"µm"/L₂)^0.5*(1-1/q^0.5))

