# ╔═╡ c1870d3b-b29f-4d1f-9928-8507b43c7390
md"""
Aplicando el teorema de Pitagoras:

$$a^2 = \left(\frac{a}{2}\right)^2 + h^2$$

despejando la altura se obtiene:

$$h^2 = a^2 -\frac{a^2}{4} = \frac{3}{4} a^2$$

$$h =\frac{\sqrt{3}}{2} a$$

El área del triángulo equilátero de la base será:

$$A_{\mathrm{triángulo}} = \frac{1}{2} a h = \frac{1}{2} a \frac{\sqrt{3}}{2} a =\frac{\sqrt{3}}{4} a^2$$

Por tanto, el volumen del prisma es:

$$V_p = a A_{\mathrm{triángulo}} = a \frac{\sqrt{3}}{4} a^2 = \frac{\sqrt{3}}{4} a^3$$

El área total del prima se compone de 3 cuadrados de lado $a$ y de dos triángulos equiláteros de lado $a$:

$$A_p = 3 a^2 + 2 A_{\mathrm{triángulo}} = 3 a^2 + \frac{\sqrt{3}}{2} a^2 = \frac{6 + \sqrt{3}}{2} a^2$$

Para encontrar el factor de forma λ, determinaremos en primer lugar $p$:

$$p = \frac{\sqrt{3}}{4}$$

El parámetro $q$ es:

$$q =\frac{6 + \sqrt{3}}{2} \frac{1}{6} = \frac{6 + \sqrt{3}}{12}$$

El factor de forma será:

$$\lambda = \frac{q}{p} = \frac{\frac{6 + \sqrt{3}}{12}}{\frac{\sqrt{3}}{4}}$$
"""

# ╔═╡ 502cca74-f75f-4d3d-a252-a66d1fffc65e
λ = ((6+sqrt(3))/12)/(sqrt(3)/4)

