Efectividad = 0.914

# ╔═╡ e6de5c54-037f-4b0d-aceb-5e631c1be582
F = 1200u"kg/hr"

# ╔═╡ 5e063758-f7c9-45c2-baf4-ac5b69058754
xF_rotas = 0.05

# ╔═╡ f4daf5df-1eec-4321-b63c-0237c372adc3
R = 80u"kg/hr"

# ╔═╡ 9ee8bf12-42e2-11ec-3586-61df0c1ed25b
md"""
### 2. Preparación de materia prima

#### Problema 3

Un clasificador óptico separa galletas rotas a la salida de un horno continuo. La alimentación al clasificador (_F_) es de $(F), con un $(Int8(xF_rotas*100)) % de las galletas rotas. Sabiendo que la eficiencia del separador es de un $(Efectividad*100) % y que el caudal del desperdicio es de $(R). ¿Cuál es la composición de corriente de producto y de residuo?

##### Solución

Los datos del problema son:
"""

# ╔═╡ eb40f2dd-8132-46c9-8826-d626fac60494
md"""
La fracción másica de galletas enteras de la alimentación es:
"""

# ╔═╡ e961a836-c1bb-4a4a-a519-2f47b9b9e364
xF = 1 - xF_rotas

# ╔═╡ 66a41413-bc22-4aa0-9ef6-197dd265a378
md"""
Realizando el balance de materia podemos encontrar el caudal másico de producto:
"""

# ╔═╡ a2124b06-e1c5-4755-9946-141cc459cc17
P = F - R

# ╔═╡ 7cb5333c-d44f-4cf0-8a19-ad41b58d9718
md"""
Realizamos el balance de materia de galletas enteras:

$$F x_F = P x_P + R x_R$$

para poder encontrar la fracción másica de la corriente de resíduo:

$$x_F = \frac{P x_P + R x_R}{F}$$

Considerando la definición de efectividad:

$$\mathrm{Efectividad} = \frac{P x_P}{F x_F} \frac{R (1-x_R)}{F(1-x_F)}$$

encontramos que tenemos un sistema de dos ecuaciones con dos incógnitas, $x_P$ y $x_R$.

Resolvemos el sistema de ecuaciones:
"""

# ╔═╡ d0b2b848-6e93-4a53-90e8-08a223396e98
@vars xP xR

# ╔═╡ 7206170b-b7e1-427d-bc45-09349ba412ce
sol = solve([xF-(P*xP+R*xR)/F,
	Efectividad-P*xP/(F*xF)*R*(1-xR)/(F*(1-xF))],
	[xP, xR])

# ╔═╡ b4f3b703-f60d-4f5c-b426-69fd2e9348c2
cm"""
Se obtienen dos soluciones al resolver el sistema de ecuaciones, pero solo la segunda tiene sentido físico. La primera tiene una fracción másica negativa y la otra con una valor superior a la unidad, ninguna de las dos fracciones tiene sentido. Como consecuencia, elegimos la segunda de las soluciones:

!!! Solución
	``x_P`` = $(round(sol[2][1]; digits=3))

	``x_R`` = $(round(sol[2][2]; digits=3))
"""

